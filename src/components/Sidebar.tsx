import { Link, useLocation, useNavigate } from 'react-router-dom'
import { useAuth } from '@/contexts/AuthContext'
import { useSite } from '@/contexts/SiteContext'
import { useSites } from '@/queries/sites'
import { useTranslation } from 'react-i18next'
import Dropdown, { DropdownItem, DropdownDivider } from '@/components/Dropdown'

interface SidebarProps {
  isOpen: boolean
  onClose: () => void
}

export default function Sidebar({ isOpen, onClose }: SidebarProps) {
  const location = useLocation()
  const navigate = useNavigate()
  const { user, logout } = useAuth()
  const { selectedSiteId, setSelectedSiteId } = useSite()
  const { data: sitesData } = useSites({ page: 1, pageSize: 100 })
  const { t, i18n } = useTranslation()
  const language = i18n.language === 'es' ? 'es' : 'en'

  const hasSites = sitesData && sitesData.data.length > 0

  const navItems = [
    { name: t('nav.home'), path: '/', icon: HomeIcon },
    { name: t('nav.dashboard'), path: '/dashboard', icon: DashboardIcon },
    { name: t('nav.languages'), path: '/languages', icon: LanguagesIcon },
    { name: t('nav.departments'), path: '/departments', icon: DepartmentsIcon },
    { name: t('nav.profiles'), path: '/profiles', icon: ProfilesIcon },
    { name: t('nav.jobTitles'), path: '/job-titles', icon: JobTitlesIcon },
    { name: t('nav.brands'), path: '/brands', icon: BrandsIcon },
    { name: t('nav.products'), path: '/products', icon: ProductsIcon },
    { name: t('nav.recipes'), path: '/recipes', icon: RecipesIcon },
    { name: t('nav.news'), path: '/news', icon: NewsIcon },
    { name: t('nav.multimedia'), path: '/multimedia', icon: MultimediaIcon },
    { name: t('nav.sites'), path: '/sites', icon: SitesIcon },
    { name: t('nav.pages'), path: '/pages', icon: PagesIcon },
  ]

  const isActive = (path: string) => location.pathname === path

  const isNavItemDisabled = (path: string) => {
    // Disable Pages if no site is selected
    if (path === '/pages' && !selectedSiteId) {
      return true
    }
    return false
  }

  const handleLogout = () => {
    logout()
    navigate('/login')
    onClose()
  }

  const handleProfileClick = () => {
    navigate('/profile')
    onClose()
  }

  return (
    <>
      {/* Mobile overlay */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black/50 z-40 lg:hidden"
          onClick={onClose}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`fixed top-0 left-0 h-full w-64 bg-card border-r border-border z-50 transform transition-transform duration-300 ease-in-out lg:translate-x-0 ${
          isOpen ? 'translate-x-0' : '-translate-x-full'
        }`}
      >
        <div className="flex flex-col h-full">
          {/* Logo/Brand */}
          <div className="flex items-center justify-between h-16 px-6 border-b border-border">
            <h2 className="text-xl font-bold text-primary">CMS</h2>
            <button
              onClick={onClose}
              className="lg:hidden text-muted-foreground hover:text-foreground"
              aria-label="Close sidebar"
            >
              <svg
                xmlns="http://www.w3.org/2000/svg"
                fill="none"
                viewBox="0 0 24 24"
                strokeWidth={1.5}
                stroke="currentColor"
                className="w-6 h-6"
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  d="M6 18L18 6M6 6l12 12"
                />
              </svg>
            </button>
          </div>

          {/* Site Selector */}
          {hasSites && (
            <div className="px-4 pt-4 pb-2 border-b border-border">
              <label className="block text-xs font-medium text-muted-foreground mb-2">
                {t('nav.selectSite') || 'Select Site'}
              </label>
              <select
                value={selectedSiteId || ''}
                onChange={(e) => setSelectedSiteId(e.target.value || null)}
                className="w-full px-3 py-2 bg-background border border-border rounded-lg focus:outline-none focus:ring-2 focus:ring-primary text-sm"
              >
                <option value="">
                  {t('nav.noSiteSelected') || 'No site selected'}
                </option>
                {sitesData.data.map((site) => (
                  <option key={site.id} value={site.id}>
                    {site.name}
                  </option>
                ))}
              </select>
            </div>
          )}

          {/* Navigation */}
          <nav className="flex-1 px-4 py-6 space-y-2 overflow-y-auto">
            {navItems.map((item) => {
              const Icon = item.icon
              const disabled = isNavItemDisabled(item.path)

              if (disabled) {
                return (
                  <div
                    key={item.path}
                    className="flex items-center gap-3 px-4 py-3 rounded-lg text-muted-foreground/50 cursor-not-allowed opacity-50"
                    title={item.path === '/pages' ? (t('nav.selectSiteFirst') || 'Please select a site first') : ''}
                  >
                    <Icon className="w-5 h-5" />
                    <span>{item.name}</span>
                    {item.path === '/pages' && (
                      <LockIcon className="w-4 h-4 ml-auto" />
                    )}
                  </div>
                )
              }

              return (
                <Link
                  key={item.path}
                  to={item.path}
                  onClick={onClose}
                  className={`flex items-center gap-3 px-4 py-3 rounded-lg transition-colors ${
                    isActive(item.path)
                      ? 'bg-primary/10 text-primary font-medium'
                      : 'text-muted-foreground hover:bg-secondary hover:text-foreground'
                  }`}
                >
                  <Icon className="w-5 h-5" />
                  <span>{item.name}</span>
                </Link>
              )
            })}
          </nav>

          {/* User Profile Section */}
          <div className="p-4 border-t border-border">
            <Dropdown
              trigger={
                <button className="flex items-center gap-3 w-full p-3 rounded-lg hover:bg-secondary transition-colors text-left">
                  <div className="w-10 h-10 rounded-full bg-linear-to-br from-primary-400 to-accent-400 flex items-center justify-center text-white font-semibold shrink-0">
                    {user?.name?.charAt(0).toUpperCase()}
                  </div>
                  <div className="flex-1 min-w-0">
                    <p className="text-sm font-medium text-card-foreground truncate">
                      {user?.name}
                    </p>
                    <p className="text-xs text-muted-foreground truncate">
                      {user?.email}
                    </p>
                  </div>
                  <svg
                    xmlns="http://www.w3.org/2000/svg"
                    fill="none"
                    viewBox="0 0 24 24"
                    strokeWidth={1.5}
                    stroke="currentColor"
                    className="w-4 h-4 text-muted-foreground shrink-0"
                  >
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      d="M8.25 15L12 18.75 15.75 15m-7.5-6L12 5.25 15.75 9"
                    />
                  </svg>
                </button>
              }
            >
              <DropdownItem
                onClick={() => {
                  navigate('/settings')
                  onClose()
                }}
                icon={<SettingsIcon className="w-4 h-4" />}
              >
                {t('menu.settings')}
              </DropdownItem>
              <DropdownItem
                onClick={handleProfileClick}
                icon={<ProfileIcon className="w-4 h-4" />}
              >
                {t('menu.profile')}
              </DropdownItem>
              <DropdownDivider />
              <DropdownItem
                onClick={() => i18n.changeLanguage(language === 'en' ? 'es' : 'en')}
                icon={<LanguageIcon className="w-4 h-4" />}
              >
                {t('menu.language')}: {language === 'en' ? t('lang.english') : t('lang.spanish')}
              </DropdownItem>
              <DropdownDivider />
              <DropdownItem
                onClick={handleLogout}
                icon={<LogoutIcon className="w-4 h-4" />}
                variant="danger"
              >
                {t('menu.logout')}
              </DropdownItem>
            </Dropdown>
          </div>
        </div>
      </aside>
    </>
  )
}

